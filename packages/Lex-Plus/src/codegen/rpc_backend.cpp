std::string RPCBackend::generate_python() {
    std::stringstream output;
    output << generate_python_types();
    output << "\n\n";
    output << generate_python_handlers();
    return output.str();
}

std::string RPCBackend::generate_python_types() {
    std::stringstream output;
    
    output << "\"\"\"\n";
    output << "Aurelius RPC Protocol - Auto-generated types\n";
    output << "Generated by Lex Compiler v" << LEX_VERSION << "\n";
    output << "\"\"\"\n\n";
    
    output << "from dataclasses import dataclass\n";
    output << "from typing import List, Optional, Dict, Any\n\n";
    
    for (const auto& [name, s] : structs_) {
        output << "@dataclass\n";
        output << "class " << name << ":\n";
        
        if (s.field_names.empty()) {
            output << "    pass\n";
        } else {
            for (size_t i = 0; i < s.field_names.size(); i++) {
                const auto& fname = s.field_names[i];
                const auto& ftype = s.field_types[i];
                bool is_opt = s.field_optional[i];
                
                output << "    " << fname << ": ";
                if (is_opt) {
                    output << "Optional[" << python_type(ftype) << "]";
                } else {
                    output << python_type(ftype);
                }
                output << "\n";
            }
        }
        output << "\n";
    }
    
    return output.str();
}

std::string RPCBackend::generate_python_handlers() {
    std::stringstream output;
    
    output << "\"\"\"\n";
    output << "Aurelius RPC Protocol - Auto-generated handlers\n";
    output << "Generated by Lex Compiler v" << LEX_VERSION << "\n";
    output << "\"\"\"\n\n";
    
    output << "from .types import *\n";
    output << "from .server import JsonRpcServer\n\n";
    
    // Generate handler interfaces
    output << "class RPCHandlerBase:\n";
    output << "    \"\"\"Base class for RPC handlers. Override methods to implement.\"\"\"\n\n";
    
    for (const auto& method : methods_) {
        std::string snake_name = to_snake_case(method.name);
        output << "    def handle_" << snake_name << "(self, params: dict) -> dict:\n";
        output << "        \"\"\"Handle " << method.name << " method.\"\"\"\n";
        output << "        raise NotImplementedError(\"" << method.name << " not implemented\")\n\n";
    }
    
    // Generate registration function
    output << "\n\ndef register_handlers(server: JsonRpcServer, handler: RPCHandlerBase):\n";
    output << "    \"\"\"Register all RPC handlers with the server.\"\"\"\n\n";
    
    for (const auto& method : methods_) {
        std::string snake_name = to_snake_case(method.name);
        output << "    def handle_" << snake_name << "_wrapper(params: dict) -> dict:\n";
        output << "        return handler.handle_" << snake_name << "(params)\n";
        output << "    server.register(\"" << method.name << "\", handle_" << snake_name << "_wrapper)\n\n";
    }
    
    return output.str();
}

std::string RPCBackend::python_type(const std::string& lex_type) {
    if (lex_type == "string") return "str";
    if (lex_type == "int") return "int";
    if (lex_type == "float") return "float";
    if (lex_type == "bool" || lex_type == "boolean") return "bool";
    if (lex_type == "Empty") return "None";
    
    // Array type: "Type[]"
    if (lex_type.size() >= 2 && lex_type.substr(lex_type.size() - 2) == "[]") {
        std::string inner = lex_type.substr(0, lex_type.size() - 2);
        return "List[" + python_type(inner) + "]";
    }
    
    // Custom type
    return lex_type;
}

