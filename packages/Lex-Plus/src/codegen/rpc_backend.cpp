std::string RPCBackend::generate(const std::vector<std::unique_ptr<Definition>>& ast) {
    parse_definitions(ast);
    
    std::stringstream output;
    
    output << "// ===========================================================================\n";
    output << "// Generated by Lex Compiler v" << LEX_VERSION << "\n";
    output << "// RPC Protocol - Python + Go\n";
    output << "// ===========================================================================\n\n";
    
    output << "=== PYTHON ===\n\n";
    output << generate_python();
    
    output << "\n=== GO ===\n\n";
    output << generate_go();
    
    return output.str();
}

void RPCBackend::parse_definitions(const std::vector<std::unique_ptr<Definition>>& ast) {
    for (const auto& def : ast) {
        if (def->definition_type == "method") {
            parse_method(*def);
        } else if (def->definition_type == "struct") {
            parse_struct(*def);
        }
    }
}

void RPCBackend::parse_method(const Definition& def) {
    MethodDef method;
    method.name = def.identifier;
    
    for (const auto& prop : def.properties) {
        if (!prop->value || !prop->value->expression) continue;
        
        if (prop->name == "input") {
            method.input_type = prop->value->expression->reference;
        } else if (prop->name == "output") {
            method.output_type = prop->value->expression->reference;
        }
    }
    
    methods_.push_back(method);
}

void RPCBackend::parse_struct(const Definition& def) {
    StructDef s;
    s.name = def.identifier;
    
    for (const auto& prop : def.properties) {
        if (!prop->value || !prop->value->expression) continue;
        
        std::string type;
        bool optional = false;
        
        // Get type from expression
        auto& expr = prop->value->expression;
        if (expr->type == Expression::Type::REFERENCE) {
            type = expr->reference;
        } else if (expr->type == Expression::Type::STRING) {
            type = std::get<std::string>(expr->value);
        }
        
        // Check for optional (?) suffix
        if (!type.empty() && type.back() == '?') {
            optional = true;
            type.pop_back();
        }
        
        s.field_names.push_back(prop->name);
        s.field_types.push_back(type);
        s.field_optional.push_back(optional);
    }
    
    structs_[s.name] = s;
}

