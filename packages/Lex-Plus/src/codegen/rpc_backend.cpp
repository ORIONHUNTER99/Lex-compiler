std::string RPCBackend::strip_optional(const std::string& type) {
    if (!type.empty() && type.back() == '?') {
        return type.substr(0, type.size() - 1);
    }
    return type;
}

std::string RPCBackend::to_snake_case(const std::string& str) {
    std::string result;
    for (size_t i = 0; i < str.size(); i++) {
        char c = str[i];
        if (c == '/' || c == '.') {
            result += '_';
        } else if (std::isupper(static_cast<unsigned char>(c)) && i > 0) {
            result += '_';
            result += static_cast<char>(std::tolower(static_cast<unsigned char>(c)));
        } else {
            result += static_cast<char>(std::tolower(static_cast<unsigned char>(c)));
        }
    }
    return result;
}

std::string RPCBackend::to_pascal_case(const std::string& str) {
    std::string snake = to_snake_case(str);
    std::string result;
    bool capitalize = true;
    
    for (char c : snake) {
        if (c == '_') {
            capitalize = true;
        } else {
            if (capitalize) {
                result += static_cast<char>(std::toupper(static_cast<unsigned char>(c)));
                capitalize = false;
            } else {
                result += c;
            }
        }
    }
    
    return result;
}

