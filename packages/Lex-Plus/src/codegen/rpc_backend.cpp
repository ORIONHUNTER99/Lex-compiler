std::string RPCBackend::generate_go() {
    std::stringstream output;
    output << generate_go_types();
    output << "\n\n";
    output << generate_go_client();
    return output.str();
}

std::string RPCBackend::generate_go_types() {
    std::stringstream output;
    
    output << "// Code generated by Lex Compiler v" << LEX_VERSION << ". DO NOT EDIT.\n";
    output << "// Package rpc provides JSON-RPC types for Aurelius.\n\n";
    
    output << "package rpc\n\n";
    
    for (const auto& [name, s] : structs_) {
        output << "// " << name << " represents the " << name << " RPC type.\n";
        output << "type " << name << " struct {\n";
        
        for (size_t i = 0; i < s.field_names.size(); i++) {
            const auto& fname = s.field_names[i];
            const auto& ftype = s.field_types[i];
            bool is_opt = s.field_optional[i];
            
            std::string go_name = to_pascal_case(fname);
            std::string type_str = go_type(ftype);
            
            if (is_opt && !type_str.empty() && type_str[0] != '*' && type_str != "interface{}") {
                type_str = "*" + type_str;
            }
            
            output << "    " << go_name << " " << type_str;
            output << " `json:\"" << fname << "\"`\n";
        }
        output << "}\n\n";
    }
    
    return output.str();
}

std::string RPCBackend::generate_go_client() {
    std::stringstream output;
    
    output << "// Code generated by Lex Compiler v" << LEX_VERSION << ". DO NOT EDIT.\n\n";
    
    output << "package rpc\n\n";
    
    output << "import \"context\"\n\n";
    
    output << "// Client provides JSON-RPC client methods.\n";
    output << "type Client interface {\n";
    
    for (const auto& method : methods_) {
        std::string req_type = method.input_type.empty() ? "struct{}" : method.input_type;
        std::string res_type = method.output_type.empty() ? "struct{}" : method.output_type;
        std::string func_name = to_pascal_case(to_snake_case(method.name));
        
        output << "    " << func_name << "(ctx context.Context, req *" << req_type << ") (*" << res_type << ", error)\n";
    }
    
    output << "}\n\n";
    
    return output.str();
}

std::string RPCBackend::go_type(const std::string& lex_type) {
    if (lex_type == "string") return "string";
    if (lex_type == "int") return "int64";
    if (lex_type == "float") return "float64";
    if (lex_type == "bool" || lex_type == "boolean") return "bool";
    if (lex_type == "Empty") return "struct{}";
    
    // Array type: "Type[]"
    if (lex_type.size() >= 2 && lex_type.substr(lex_type.size() - 2) == "[]") {
        std::string inner = lex_type.substr(0, lex_type.size() - 2);
        return "[]" + go_type(inner);
    }
    
    // Custom type
    return lex_type;
}

