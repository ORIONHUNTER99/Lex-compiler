std::string LuaBackend::generate_expression(const Expression* expr, bool in_condition) {
    if (!expr) return "nil";

    switch (expr->type) {
        case Expression::Type::INTEGER:
            return std::to_string(std::get<int64_t>(expr->value));

        case Expression::Type::FLOAT:
            return std::to_string(std::get<double>(expr->value));

        case Expression::Type::STRING:
            return "\"" + std::get<std::string>(expr->value) + "\"";

        case Expression::Type::BOOLEAN:
            return std::get<bool>(expr->value) ? "true" : "false";

        case Expression::Type::REFERENCE:
            // In conditions: game_state lookup for runtime evaluation
            // In properties: string for simple data storage
            if (in_condition) {
                return "game_state." + expr->reference;
            }
            return "\"" + expr->reference + "\"";

        case Expression::Type::BINARY:
            return generate_binary_expression(expr, in_condition);

        case Expression::Type::UNARY:
            return generate_unary_expression(expr, in_condition);

        case Expression::Type::CALL:
            return generate_call_expression(expr, in_condition);

        case Expression::Type::MEMBER:
            return generate_member_expression(expr, in_condition);

        case Expression::Type::COLOR:
            if (std::holds_alternative<std::string>(expr->value)) {
                return "\"" + std::get<std::string>(expr->value) + "\"";
            }
            return "\"\"";

        case Expression::Type::NULL_VAL:
        default:
            return "nil";
    }
}

std::string LuaBackend::generate_binary_expression(const Expression* expr, bool in_condition) {
    if (!expr || !expr->left || !expr->right) return "nil";

    std::string left = generate_expression(expr->left.get(), in_condition);
    std::string right = generate_expression(expr->right.get(), in_condition);
    std::string op = binary_op_to_lua(expr->binary_op);

    return "(" + left + " " + op + " " + right + ")";
}

std::string LuaBackend::generate_unary_expression(const Expression* expr, bool in_condition) {
    if (!expr || !expr->operand) return "nil";

    std::string operand = generate_expression(expr->operand.get(), in_condition);
    std::string op = unary_op_to_lua(expr->unary_op);

    return "(" + op + operand + ")";
}

std::string LuaBackend::generate_call_expression(const Expression* expr, bool in_condition) {
    if (!expr) return "nil";

    std::string result = expr->function_name + "(";
    bool first = true;
    for (const auto& arg : expr->arguments) {
        if (!first) result += ", ";
        result += generate_expression(arg.get(), in_condition);
        first = false;
    }
    result += ")";
    return result;
}

std::string LuaBackend::generate_member_expression(const Expression* expr, bool in_condition) {
    if (!expr || !expr->object) return "nil";

    std::string obj = generate_expression(expr->object.get(), in_condition);
    return obj + "." + expr->member_name;
}

std::string LuaBackend::generate_condition(const Condition* cond) {
    if (!cond) return "";

    std::stringstream lua;
    lua << "        " << cond->trigger << " = ";

    // If we have an actual expression, use it
    if (cond->expression && cond->expression->type != Expression::Type::BOOLEAN) {
        lua << "function(game_state) return "
            << generate_expression(cond->expression.get(), true)
            << " end,\n";
        return lua.str();
    }

    // Check if this is a property-style condition (available_if: { technologies: [...] })
    if (!cond->properties.empty()) {
        // Build condition from properties
        std::vector<std::string> checks;
        
        for (const auto& prop : cond->properties) {
            if (prop->name == "technologies" && prop->value) {
                // technologies: [TechA, TechB] -> has_technology("TechA") and has_technology("TechB")
                if (prop->value->type == PropertyValue::Type::REFERENCE_LIST && 
                    prop->value->reference_list) {
                    for (const auto& tech : prop->value->reference_list->references) {
                        checks.push_back("has_technology(\"" + tech + "\")");
                    }
                }
            } else if (prop->name == "structures" && prop->value) {
                // structures: [BuildingA] -> has_structure("BuildingA")
                if (prop->value->type == PropertyValue::Type::REFERENCE_LIST && 
                    prop->value->reference_list) {
                    for (const auto& struct_id : prop->value->reference_list->references) {
                        checks.push_back("has_structure(\"" + struct_id + "\")");
                    }
                }
            } else if (prop->name == "resources" && prop->value) {
                // resources: { gold: 50 } -> has_resources({gold = 50})
                if (prop->value->type == PropertyValue::Type::RESOURCE_MAP && 
                    prop->value->resource_map) {
                    checks.push_back("has_resources(" + 
                        generate_resource_map(*prop->value->resource_map) + ")");
                }
            }
        }
        
        if (!checks.empty()) {
            std::string condition_expr;
            for (size_t i = 0; i < checks.size(); ++i) {
                if (i > 0) condition_expr += " and ";
                condition_expr += checks[i];
            }
            lua << "function(game_state) return " << condition_expr << " end,\n";
            return lua.str();
        }
    }

    // Fallback: use expression (might be placeholder true)
    if (cond->expression) {
        lua << "function(game_state) return "
            << generate_expression(cond->expression.get(), true)
            << " end,\n";
    } else {
        lua << "true,\n";
    }

    return lua.str();
}

std::string LuaBackend::binary_op_to_lua(Expression::BinaryOp op) const {
    switch (op) {
        case Expression::BinaryOp::ADD: return "+";
        case Expression::BinaryOp::SUB: return "-";
        case Expression::BinaryOp::MUL: return "*";
        case Expression::BinaryOp::DIV: return "/";
        case Expression::BinaryOp::MOD: return "%";
        case Expression::BinaryOp::EQ: return "==";
        case Expression::BinaryOp::NE: return "~=";
        case Expression::BinaryOp::GT: return ">";
        case Expression::BinaryOp::LT: return "<";
        case Expression::BinaryOp::GE: return ">=";
        case Expression::BinaryOp::LE: return "<=";
        case Expression::BinaryOp::AND: return "and";
        case Expression::BinaryOp::OR: return "or";
        default: return "+";
    }
}

std::string LuaBackend::unary_op_to_lua(Expression::UnaryOp op) const {
    switch (op) {
        case Expression::UnaryOp::NOT: return "not ";
        case Expression::UnaryOp::NEG: return "-";
        default: return "";
    }
}

